package Lista2;

import java.util.Scanner;

public class Exercício4 {
	//Faça um programa em que permita a entrada de um número qualquer e exiba se 
	//este número é par ou ímpar. Se for par exiba também a raiz quadrada do mesmo; 
	//se for ímpar exiba o número elevado ao quadrado.

		public static void main(String[] args) {
			Scanner read=new Scanner(System.in);
			int numero;
			
			System.out.println("Digite um número: ");
			numero = read.nextInt();
			if ((numero%2)==0) {
				System.out.printf("O número é par e a sua raiz quadrada é: %.2f\n",Math.sqrt(numero));
				
			}
			else {
				System.out.printf("O número é impar e seu quadrado é: %.2f\n",Math.pow(numero,2));
			}
		}		
}
